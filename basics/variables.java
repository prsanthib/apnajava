// variables are containers to store a value
//depending on the size of the value the memory can be allocated
// we can change the value of a variable 
class variables
{
public static void main (String args[])
{
    int a = 10;
    int b = 20;
    String d = "PRASANTHI";
    System.out.println(d);
    System.out.println(a+b);
    System.out.println(a+b+d);
    
    a = 50;
    a = b;
    System.out.println(a+b);
}
}