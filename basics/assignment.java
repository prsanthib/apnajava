class assignment
{
    public static void main (String args[])
    {
        int a = 10;
        int b = 20;
        System.out.println(a+=b); // a = a+b; a = 30
        System.out.println(a-=b); // a = a-b; a = 30 - 20
        System.out.println(a*=b); //a = 10*20
        System.out.println(a/=b); //200/20
        System.out.println(a%=b); // 10 % 20


    }
}